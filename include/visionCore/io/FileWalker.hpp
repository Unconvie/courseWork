#pragma once

#include <filesystem>
#include <string>
#include <unordered_set>
#include <vector>

namespace visionCore::io {

/**
 * @brief Обходит файловую систему и собирает список медиафайлов для обработки.
 *
 * Принимает на вход путь, который может указывать либо на единичный файл,
 * либо на папку:
 *  - единичный файл - возвращает его, если расширение поддерживается;
 *  - папка - возвращает все файлы из папки с поддерживаемыми расширениями.
 *
 * Класс намеренно отделён от чтения файла (за это отвечает ImageIO):
 *  - один и тот же обходчик переиспользуется для разных задач (например,
 *    для будущего генератора тестовых данных, который тоже сканирует папку),
 *  - проще тестировать каждую обязанность по отдельности.
 *
 * Список поддерживаемых расширений задаётся при создании объекта - это
 * позволяет точечно расширить набор форматов (например, добавить ".tif"
 * для астрономии) без правок кода.
 */
class FileWalker {
public:
    /**
     * @brief Создать обходчик с указанным набором расширений.
     *
     * @param extensions Множество допустимых расширений в нижнем регистре,
     *        с точкой (например {".png", ".jpg"}). При сравнении расширение
     *        файла приводится к нижнему регистру, так что "Photo.JPG" найдётся.
     * @param recursive Если true и путь указывает на папку - подпапки тоже
     *        обходятся. По умолчанию false, чтобы случайно не затащить
     *        в обработку соседние подпапки (например, build/ или output/).
     */
    explicit FileWalker(std::unordered_set<std::string> extensions,
                        bool recursive = false);

    /**
     * @brief Фабричный метод для типовых статичных изображений.
     *
     * Возвращает обходчик с набором {".png", ".jpg", ".jpeg", ".bmp",
     * ".tif", ".tiff"} - расширения, которые гарантированно понимает OpenCV
     * в стандартной сборке.
     */
    [[nodiscard]] static FileWalker defaultImageWalker();

    /**
     * @brief Собрать список файлов для обработки по указанному пути.
     *
     * @param path Путь к файлу или папке.
     * @return Отсортированный по алфавиту список путей к файлам с
     *         подходящими расширениями. Если на входе единичный файл с
     *         подходящим расширением - в результате будет один элемент.
     *
     * @note Пустой список - это НЕ ошибка для batch-сценария. Вызывающая
     *       сторона сама решает, что с ним делать (например, вывести
     *       предупреждение и завершиться).
     */
    [[nodiscard]] std::vector<std::filesystem::path>
        collect(const std::filesystem::path& path) const;

    /**
     * @brief Проверить, поддерживается ли расширение указанного пути.
     * @param path Путь, у которого извлекается и проверяется расширение.
     */
    [[nodiscard]] bool isExtensionSupported(
        const std::filesystem::path& path) const;

private:
    /// Множество расширений в нижнем регистре, с точкой.
    std::unordered_set<std::string> m_extensions;

    /// Обходить ли подпапки при сканировании директории.
    bool m_recursive;

    /**
     * @brief Привести строку к нижнему регистру.
     *
     * Нужно для регистронезависимого сравнения расширений: ".JPG" должно
     * совпадать с ".jpg". Работает корректно для ASCII-расширений, для
     * кириллицы пришлось бы тащить ICU - это не наш кейс.
     */
    [[nodiscard]] static std::string toLower(std::string s);
};

} // namespace visionCore::io
